function makeChineseQuestion(number, unit, q, correct, distractors, explain) {
  const answer = (number - 51) % 4;
  const options = distractors.slice(0, 3);
  options.splice(answer, 0, correct);

  return {
    id: `chinese-${String(number).padStart(3, "0")}`,
    unit,
    q,
    options,
    answer,
    explain
  };
}

